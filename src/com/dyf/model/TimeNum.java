package com.dyf.model;

public class TimeNum {

	private int time;
	private int num;
	
	/**
	 * @param time
	 * @param num
	 */
	public TimeNum(int num, int time) {
		super();
		this.time = time;
		this.num = num;
	}
	
	public int getTime() {
		return time;
	}


	public void setTime(int time) {
		this.time = time;
	}


	public int getNum() {
		return num;
	}


	public void setNum(int num) {
		this.num = num;
	}


	
	
	
	
}
